namespace SortArray.ArraySort.Enumeration
{
    #region Enumeration

    /// <summary>
    /// Types of operation which can be performed on an array.
    /// </summary>
    public enum SortOperation
    {
        /// <summary>
        /// Sorted array will be in ascending order.
        /// </summary>
        Ascending = 1,

        /// <summary>
        /// Sorted array will be in descending order.
        /// </summary>
        Descending = 2,
    }

    #endregion
}
